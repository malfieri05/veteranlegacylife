// Improved calculatePremium function with better error handling and interpolation
export function calculatePremium(
  gender: 'male' | 'female' | 'Male' | 'Female',
  age: number,
  coverage: number,
  healthTier: 'IUL' | 'Select 1' | 'Select 2' | 'Select 3'
): number | null {
  console.log(` calculatePremium called with:`, {
    gender,
    age,
    coverage,
    healthTier
  })
  
  // Input validation
  if (!gender || !age || !coverage || !healthTier) {
    console.log(` Invalid input parameters`)
    return null
  }
  
  // Normalize gender format
  const normalizedGender = gender.toLowerCase() === 'male' ? 'Male' : 'Female'
  
  // Validate age range
  if (age < 40 || age > 60) {
    console.log(` Age ${age} is outside valid range (40-60)`)
    return null
  }
  
  // Validate coverage range
  if (coverage < 50000 || coverage > 250000) {
    console.log(` Coverage ${coverage} is outside valid range (50000-250000)`)
    return null
  }
  
  // Round coverage to nearest 1000 to match data structure
  const roundedCoverage = Math.round(coverage / 1000) * 1000
  
  try {
    // First, try to find exact match
    const exactRate = findQuoteRate(normalizedGender, healthTier, age, roundedCoverage)
    
    if (exactRate) {
      console.log(` Exact rate found: $${exactRate.monthlyPremium.toFixed(2)}`)
      return exactRate.monthlyPremium
    }
    
    console.log(` No exact match found, attempting interpolation...`)
    
    // If no exact match, try interpolation
    const interpolatedRate = interpolatePremium(normalizedGender, healthTier, age, roundedCoverage)
    
    if (interpolatedRate !== null) {
      console.log(` Interpolated rate: $${interpolatedRate.toFixed(2)}`)
      return interpolatedRate
    }
    
    // If interpolation fails, try finding closest available configuration
    const fallbackRate = findClosestRate(normalizedGender, healthTier, age, roundedCoverage)
    
    if (fallbackRate !== null) {
      console.log(` Fallback rate: $${fallbackRate.toFixed(2)}`)
      return fallbackRate
    }
    
    console.log(` No rate could be calculated for the given parameters`)
    return null
    
  } catch (error) {
    console.error('Error in calculatePremium:', error)
    return null
  }
}

// Helper function for interpolation between available rates
function interpolatePremium(
  gender: 'Male' | 'Female',
  healthTier: 'IUL' | 'Select 1' | 'Select 2' | 'Select 3',
  age: number,
  coverage: number
): number | null {
  
  // Get available ages and coverages for this configuration
  const availableAges = getAvailableAges(gender, healthTier)
  const availableCoverages = getAvailableCoverageAmounts(gender, healthTier, age)
  
  if (availableAges.length === 0 || availableCoverages.length === 0) {
    return null
  }
  
  // Find closest ages
  const lowerAge = availableAges.filter(a => a <= age).sort((a, b) => b - a)[0]
  const upperAge = availableAges.filter(a => a >= age).sort((a, b) => a - b)[0]
  
  // Find closest coverages
  const lowerCoverage = availableCoverages.filter(c => c <= coverage).sort((a, b) => b - a)[0]
  const upperCoverage = availableCoverages.filter(c => c >= coverage).sort((a, b) => a - b)[0]
  
  // If we have exact age match, interpolate by coverage
  if (lowerAge === upperAge && lowerAge === age) {
    if (lowerCoverage === upperCoverage) {
      // Exact match
      const rate = findQuoteRate(gender, healthTier, age, coverage)
      return rate ? rate.monthlyPremium : null
    }
    
    if (lowerCoverage && upperCoverage) {
      // Interpolate between coverages
      const lowerRate = findQuoteRate(gender, healthTier, age, lowerCoverage)
      const upperRate = findQuoteRate(gender, healthTier, age, upperCoverage)
      
      if (lowerRate && upperRate) {
        const ratio = (coverage - lowerCoverage) / (upperCoverage - lowerCoverage)
        return lowerRate.monthlyPremium + (upperRate.monthlyPremium - lowerRate.monthlyPremium) * ratio
      }
    }
  }
  
  // If we have exact coverage match, interpolate by age
  if (lowerCoverage === upperCoverage && lowerCoverage === coverage) {
    if (lowerAge && upperAge && lowerAge !== upperAge) {
      const lowerRate = findQuoteRate(gender, healthTier, lowerAge, coverage)
      const upperRate = findQuoteRate(gender, healthTier, upperAge, coverage)
      
      if (lowerRate && upperRate) {
        const ratio = (age - lowerAge) / (upperAge - lowerAge)
        return lowerRate.monthlyPremium + (upperRate.monthlyPremium - lowerRate.monthlyPremium) * ratio
      }
    }
  }
  
  // Bilinear interpolation if we need to interpolate both age and coverage
  if (lowerAge && upperAge && lowerCoverage && upperCoverage && 
      lowerAge !== upperAge && lowerCoverage !== upperCoverage) {
    
    const rates = {
      ll: findQuoteRate(gender, healthTier, lowerAge, lowerCoverage),
      lu: findQuoteRate(gender, healthTier, lowerAge, upperCoverage),
      ul: findQuoteRate(gender, healthTier, upperAge, lowerCoverage),
      uu: findQuoteRate(gender, healthTier, upperAge, upperCoverage)
    }
    
    if (rates.ll && rates.lu && rates.ul && rates.uu) {
      const ageRatio = (age - lowerAge) / (upperAge - lowerAge)
      const coverageRatio = (coverage - lowerCoverage) / (upperCoverage - lowerCoverage)
      
      const interpolatedLower = rates.ll.monthlyPremium + 
        (rates.lu.monthlyPremium - rates.ll.monthlyPremium) * coverageRatio
      const interpolatedUpper = rates.ul.monthlyPremium + 
        (rates.uu.monthlyPremium - rates.ul.monthlyPremium) * coverageRatio
      
      return interpolatedLower + (interpolatedUpper - interpolatedLower) * ageRatio
    }
  }
  
  return null
}

// Helper function to find the closest available rate
function findClosestRate(
  gender: 'Male' | 'Female',
  healthTier: 'IUL' | 'Select 1' | 'Select 2' | 'Select 3',
  age: number,
  coverage: number
): number | null {
  
  const availableAges = getAvailableAges(gender, healthTier)
  
  if (availableAges.length === 0) {
    return null
  }
  
  // Find closest age
  const closestAge = availableAges.reduce((prev, curr) => 
    Math.abs(curr - age) < Math.abs(prev - age) ? curr : prev
  )
  
  const availableCoverages = getAvailableCoverageAmounts(gender, healthTier, closestAge)
  
  if (availableCoverages.length === 0) {
    return null
  }
  
  // Find closest coverage
  const closestCoverage = availableCoverages.reduce((prev, curr) => 
    Math.abs(curr - coverage) < Math.abs(prev - coverage) ? curr : prev
  )
  
  const rate = findQuoteRate(gender, healthTier, closestAge, closestCoverage)
  
  if (rate) {
    // Apply adjustment factors for age and coverage differences
    const ageDifference = Math.abs(age - closestAge)
    const coverageDifference = Math.abs(coverage - closestCoverage)
    
    let adjustedRate = rate.monthlyPremium
    
    // Age adjustment: roughly 2% per year
    if (ageDifference > 0) {
      const ageAdjustment = 1 + (ageDifference * 0.02 * (age > closestAge ? 1 : -1))
      adjustedRate *= ageAdjustment
    }
    
    // Coverage adjustment: roughly linear
    if (coverageDifference > 0) {
      const coverageRatio = coverage / closestCoverage
      adjustedRate *= coverageRatio
    }
    
    console.log(` Applied adjustments: age diff=${ageDifference}, coverage diff=${coverageDifference}`)
    
    return Math.max(adjustedRate, 0) // Ensure non-negative
  }
  
  return null
}

