// Helper function to find a specific quote rate
export function findQuoteRate(
  gender: 'Male' | 'Female',
  healthTier: 'Select 1' | 'Select 2' | 'Select 3' | 'IUL',
  age: number,
  coverageAmount: number
): QuoteRate | undefined {
  return QUOTE_RATES.find(rate => 
    rate.gender === gender &&
    rate.healthTier === healthTier &&
    rate.age === age &&
    rate.coverageAmount === coverageAmount
  );
}

// Helper function to get all available coverage amounts for a given configuration
export function getAvailableCoverageAmounts(
  gender: 'Male' | 'Female',
  healthTier: 'Select 1' | 'Select 2' | 'Select 3' | 'IUL',
  age: number
): number[] {
  const availableAmounts = QUOTE_RATES
    .filter(rate => 
      rate.gender === gender &&
      rate.healthTier === healthTier &&
      rate.age === age
    )
    .map(rate => rate.coverageAmount)
    .sort((a, b) => a - b);
  
  return [...new Set(availableAmounts)]; // Remove duplicates
}

// Helper function to get the minimum and maximum coverage amounts
export function getCoverageRange(): { min: number; max: number } {
  const amounts = QUOTE_RATES.map(rate => rate.coverageAmount);
  return {
    min: Math.min(...amounts),
    max: Math.max(...amounts)
  };
}

